#define UTF16_lambda const char16_t*,const char16_t*,string_hash,string_compare
#define UTF8_lambda const char*,const char*,string_hash,string_compare

struct string_hash {
	size_t operator()(const char *txt) const {
		return std::hash<std::string_view>{}(txt);
	}
	size_t operator()(const char16_t* txt) const {
		return std::hash<std::u16string_view>{}(txt);
	}
};

struct string_compare {
	bool operator()(const char *t1, const char *t2) const {
		return strncmp(t1, t2, strlen(t2)) == 0;
	}
	bool operator()(const char16_t* t1, const char16_t* t2) const {
		return std::char_traits<char16_t>::compare(t1, t2, std::char_traits<char16_t>::length(t1)) == 0;
	}
};

